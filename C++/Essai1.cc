#include <iostream>
using namespace std;

int main()
{
	int n;
	cout << "Entrez la valeur de n : ";
	cin >> n;	
	
	int n_carre;
	
	n_carre = n * n;
	
	cout << "La variable n contient " << n << endl;
	cout << "Le carre de " << n << " est " << n_carre << "." << endl;
	cout << "Le double de n est " << 2 * n << "." << endl;
	
	
	
	
	return 0;
}
